#!/usr/bin/env node
/* scripts/new-syndrome.mjs — Phase E.4 apex-content scaffold.

   Usage:
     npm run content:scaffold -- <syndromeId> [tier1Label] [tier2Label] ...

   Emits a ready-to-fill stub showing the apex bar:
     - REGIMEN_CONTENT block with one entry per requested tier label
     - SYNDROME_DECISION block with duration + monitoring
     - TODO markers where clinical judgment is required
     - matchAgent / matchBranch / matchCtx slots commented in
       so authors see all the cross-section linking opportunities

   The output is printed to stdout for the author to paste into the
   appropriate data file. The script does NOT write to source files
   directly — content additions are PR-reviewable, not generated
   in-tree. */

const synId = process.argv[2];
const tierLabels = process.argv.slice(3);

if(!synId) {
  console.error("Usage: npm run content:scaffold -- <syndromeId> [tier1Label] [tier2Label] ...\n");
  console.error("Example: npm run content:scaffold -- pneumonia 'First-line' 'Add MRSA'\n");
  process.exit(1);
}

const tiers = tierLabels.length > 0 ? tierLabels : ["Empiric"];

console.log(`/* Apex-content scaffold for syndrome "${synId}"
   ---------------------------------------------------
   Generated by scripts/new-syndrome.mjs. Drop the REGIMEN block
   into src/data/regimenContent.js and the DECISION block into
   src/data/syndromeDecision.js. Replace every TODO with apex-bar
   clinical content per docs/AUTHORING.md.

   Apex contract recap (audit-enforced):
     - pickIf:    ≤ 22 words; one-sentence verdict
     - whyPick:   3–5 bullets; each ≤ 14 words; ≥ 1 with **bold**
     - watchOut:  ≥ 2 entries; sev: stop|warn|note; ≤ 22 words each
     - branches:  every days field carries explicit unit
     - monitoring.items: ≥ 3; sev: required|trigger|consider

   Add matchAgent / matchBranch / matchCtx tags wherever a ctx field
   or sibling selection should elevate the item — see § 4–5 of
   docs/AUTHORING.md for the full predicate grammar. */

// ────────────────────────────────────────────────────────────
// Paste into src/data/regimenContent.js inside REGIMEN_CONTENT:
// ────────────────────────────────────────────────────────────

${synId.includes("-") ? `"${synId}"` : synId}: {
${tiers.map(label => `  ${JSON.stringify(label)}: [
    {
      // TODO replace with actual drug-name anchor regex
      rx: /TODO-DRUG-NAME/i,

      // TODO ≤ 22-word 5-second verdict — when to pick this option
      pickIf: "TODO: when to pick this card",

      whyPick: [
        // TODO 3–5 bullets, each ≤ 14 words; ≥ 1 with **bold**
        "**TODO killer fact** explaining why this drug here",
        "TODO second strength",
        "TODO third strength",
      ],

      watchOut: [
        // TODO ≥ 2 entries with severity tags
        { sev: "stop", text: "**TODO hard contraindication trigger** — why",
          // matchCtx: { crcl: { lt: 30 } },          // optional ctx-aware
          // matchAgent: /OTHER-AGENT-PATTERN/i,      // optional cross-link
        },
        { sev: "warn", text: "**TODO important interaction** — why" },
        { sev: "note", text: "TODO useful context that doesn't change action" },
      ],
    },
  ],`).join("\n")}
},

// ─────────────────────────────────────────────────────────────
// Paste into src/data/syndromeDecision.js inside SYNDROME_DECISION:
// ─────────────────────────────────────────────────────────────

${synId.includes("-") ? `"${synId}"` : synId}: {
  duration: {
    // TODO ≤ 22-word chart-ready bottom line
    headline: "TODO: standard duration; extension by clinical state",

    // TODO trial / guideline anchor
    evidence: "TODO trial year — finding",

    branches: [
      // TODO ≥ 1 branch; every days string carries an EXPLICIT unit
      {
        label: "Uncomplicated, source controlled",
        days: "TODO d",         // e.g. "7 d", "1 dose", "4–6 wk", "Indefinite"
        detail: "From first effective dose; standard minimum",
        // matchAgent: /AGENT-PATTERN/i,    // auto-light this branch on pick
      },
      {
        label: "Complicated / extended",
        days: "TODO d",
        detail: "Trigger criteria — what makes it complicated",
      },
    ],

    stopWhen: [
      // TODO ≥ 3 verifiable criteria; AND-joined
      "Afebrile ≥ 48 h",
      "Source identified + controlled",
      "Clinical improvement (WBC trending down, off pressors)",
    ],

    extendIf: [
      // String OR { text, matchCtx? } object form
      "Trigger 1 — what makes the course extend",
      { text: "**Severe disease** at presentation — slower clearance",
        matchCtx: { severe: true } },
    ],
  },

  monitoring: {
    // TODO ≤ 22-word headline
    headline: "TODO: what to order today",

    items: [
      // TODO ≥ 3 items with severity tags
      { sev: "required", what: "**TODO must-do order**",
        why: "Why it matters at this site" },
      { sev: "trigger", what: "Conditional order **if X**",
        why: "What X means clinically",
        // matchCtx: { severe: true },
        // matchAgent: /vancomycin/i,
        // matchBranch: ["Complicated / extended"],
      },
      { sev: "consider", what: "Optional escalation pathway",
        why: "When it changes management" },
    ],
  },
},

/* ─── Post-paste checklist ───────────────────────────────────────
   [ ] All TODOs replaced with apex-bar content
   [ ] npm run content:audit passes (full + this syndrome focus)
   [ ] Visual review at /?bedside=1#t=empiric&syn=${synId}
   [ ] matchAgent regexes match the splitter output (see existing
       syndromes for working examples)
   [ ] matchBranch labels reference real branch labels above
   [ ] matchCtx fields are in the known set (see docs/AUTHORING.md § 5)
   ─────────────────────────────────────────────────────────────── */
`);
